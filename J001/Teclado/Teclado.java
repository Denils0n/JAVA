class Teclado{
	
	String tamanho;
	String cor;
	String tipo;
	boolean tecladoNumerico;


	public void status()  {
		

		System.out.println("Teclado");
		System.out.println("Comprimento do teclado: " + this.tamanho);
		System.out.println("Cor: " + this.cor);
		System.out.println("Tipo: " + this.tipo);
		System.out.println("Tem Teclado Numerico :" + this.tecladoNumerico);



	}


}